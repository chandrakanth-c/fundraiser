    public void populateOpenTasksTable() {
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        
        model.setRowCount(0);
        
        DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
        
        for(WorkRequest request : this.fundRaiserOrg.getWorkQueue().getWorkRequestList()){
            if(request instanceof SplitVolunteerRequest && ((SplitVolunteerRequest)request).getTaskStatus().equals(BusinessStatus.ReadyToPick.getValue())) {
                
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = ((Application)request).getCause();
            row[2] = ((SplitVolunteerRequest)request).getSplitAmount();
            if(((Application)request).getRequiredBy() != null){
                row[3] = dateFormat.format(((Application)request).getRequiredBy());
            }
            
            model.addRow(row);
            }
        }
    }
    
    public void populateAssignedTasks() {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : this.fundRaiserOrg.getWorkQueue().getWorkRequestList()){
            
            if(request instanceof SplitVolunteerRequest) {
                String taskStatus = ((SplitVolunteerRequest)request).getTaskStatus();
                if(((taskStatus.equals(BusinessStatus.InProgress.getValue())) ||
                     (taskStatus.equals(BusinessStatus.Fulfilled.getValue())) ||
                     (taskStatus.equals(BusinessStatus.UnFulfilled.getValue())) ) )  {
                
                    Object[] row = new Object[7];
                    row[0] = request;
                    row[1] = ((Application)request).getCause();
                    row[2] = ((SplitVolunteerRequest)request).getSplitAmount();
                    row[3] = ((SplitVolunteerRequest)request).getAmountAchieved();
                    row[4] = taskStatus;
                    row[5] = ((SplitVolunteerRequest)request).getAssignedToVolunteer().getUsername();
                    row[6] = ((SplitVolunteerRequest)request).getSplitAmountRequired();
                    model.addRow(row);
                }
            }
                    
        }
    }
