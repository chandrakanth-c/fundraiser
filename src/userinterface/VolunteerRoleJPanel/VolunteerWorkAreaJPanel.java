    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable2.getSelectedRow();

        if (selectedRow >= 0) {

            WorkRequest wr = (WorkRequest) jTable2.getValueAt(selectedRow, 0);
            ((SplitVolunteerRequest)wr).setTaskStatus(BusinessStatus.InProgress.getValue());
            ((SplitVolunteerRequest)wr).setAssignedToVolunteer((VolunteerAccount)userAccount);
            ((VolunteerAccount)userAccount).addAssignedSplitRequestList((SplitVolunteerRequest)wr);
            JOptionPane.showMessageDialog(null, "Successfully assigned the task to you");
            logger.info("Successfully assigned the task to "+userAccount.getEmployee().getName());
            populateAssignedTasks();
            populateOpenTasksTable();
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

